function importantClicked() {

	document.getElementsByTagName('body')[0].style.backgroundColor = "red";
	document.getElementsByTagName('div')[0].style.color = "white";
	document.getElementsByTagName('h3')[0].style.color = "darkrd";

}

function normalClicked() {
	
	document.getElementsByTagName('body')[0].style.backgroundColor = "lightblue";
	document.getElementsByTagName('div')[0].style.color = "white";
	document.getElementsByTagName('h3')[0].style.color = "darkblue";

}

function restClicked() {
	
	document.getElementsByTagName('body')[0].style.backgroundColor = "white";
	document.getElementsByTagName('div')[0].style.color = "darkgrey";

}